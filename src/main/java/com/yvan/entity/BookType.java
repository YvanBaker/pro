package com.yvan.entity;


public class BookType {

  private int id;
  private String btid;
  private String name;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public String getBtid() {
    return btid;
  }

  public void setBtid(String btid) {
    this.btid = btid;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
